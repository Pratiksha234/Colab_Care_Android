class Meditation {
  final String title;
  final String description;
  final int duration;
  final String track;
  final String trackExtension;
  final String image;
  final String backgroundVideo;

  Meditation({
    required this.title,
    required this.description,
    required this.duration,
    required this.track,
    required this.trackExtension,
    required this.image,
    required this.backgroundVideo,
  });
}